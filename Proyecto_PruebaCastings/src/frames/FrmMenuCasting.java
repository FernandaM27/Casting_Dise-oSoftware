    private void mostrarP_RegistroCastings(){
        this.listaAgentes=new ArrayList<>();
        this.listaClientes=new ArrayList<>();
        this.buscarAgentes();
        this.buscarClientes();
        if(listaClientes.isEmpty()){
            this.mostrarMsj("No existe alg√∫n cliente registrado", 1);
            FrmRegistrarCliente.getFrmRegistrarCliente().setVisible(true);
            this.setVisible(false);
        }else if(listaAgentes.isEmpty()){
            this.mostrarMsj("No existe alg√∫n agente registrado", 1);
            FrmAgente.getFrmAgente().setVisible(true);
            this.setVisible(false);
        }else{
            FrmRegistrarCasting.getFrmRegistrarCasting().setListas(listaClientes, listaAgentes);
            FrmRegistrarCasting.getFrmRegistrarCasting().setVisible(true);
            this.setVisible(false);
        }
    }
    
    private void buscarAgentes(){
        this.listaAgentes = this.negocio.buscarAgentes();
    }
              
    private void buscarClientes(){
        this.listaClientes = this.negocio.buscarClientes();
    }
    
